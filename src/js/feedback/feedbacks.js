const toDataUri = svg =>
  `data:image/svg+xml;charset=utf-8,${encodeURIComponent(svg)}`;

const STAR_ON = toDataUri(STAR_ON_SVG);
const STAR_OFF = toDataUri(STAR_OFF_SVG);
const STAR_HALF = toDataUri(STAR_HALF_SVG);

export function initFeedbackRatings() {
  const ratingEls = document.querySelectorAll('.feedback-stars-rating');

  ratingEls.forEach(el => {
    if (el.dataset.ratyInitialized === 'true') return;

    let score = Number(el.dataset.score);
    if (Number.isNaN(score)) score = 0;
    score = Math.max(0, Math.min(5, score));

    const raty = new Raty(el, {
      readOnly: true,
      score,
      number: 5,
      halfShow: true,

      starType: 'img',
      starOn: STAR_ON,
      starOff: STAR_OFF,
      starHalf: STAR_HALF,

      space: false,
    });

    raty.init?.();

    el.dataset.ratyInitialized = 'true';
  });
}

